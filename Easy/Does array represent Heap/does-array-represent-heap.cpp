class Solution{
    public:
    bool isMaxHeap(int arr[], int n)
    {
        // Your code goes here
        int i=0;int j=1;
        while(i<n)
        {
            if(j<n&&arr[i]<arr[j])return false;
            if(j+1<n&&arr[i]<arr[j])return false;
            i++;
            j+=2;
            if(j>=n)return true;
        }
        return true;
    }
};

